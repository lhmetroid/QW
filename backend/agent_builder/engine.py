def get_custom_rag_context(db: Session, agent_id: str, query: str) -> str:
    """
    Search database for knowledge chunks corresponding to agent_id,
    calculate cosine similarity against query embedding, and return formatted context.
    """
    try:
        chunks = db.query(BuilderKnowledgeChunk).join(
            BuilderKnowledge, BuilderKnowledge.knowledge_id == BuilderKnowledgeChunk.knowledge_id
        ).filter(BuilderKnowledge.agent_id == agent_id).all()

        if not chunks:
            return ""

        query_vector = EmbeddingService.embed(query)
        if not query_vector:
            return ""

        scored_chunks = []
        for c in chunks:
            if not c.embedding:
                continue
            dot = sum(a * b for a, b in zip(query_vector, c.embedding))
            norm_a = math.sqrt(sum(a * a for a in query_vector))
            norm_b = math.sqrt(sum(b * b for b in c.embedding))
            if norm_a == 0 or norm_b == 0:
                sim = 0.0
            else:
                sim = dot / (norm_a * norm_b)
            if sim > 0.35:
                scored_chunks.append((sim, c.content))

        scored_chunks.sort(key=lambda x: x[0], reverse=True)
        top_chunks = scored_chunks[:3]

        if not top_chunks:
            return ""

        context_lines = []
        for idx, (sim, text) in enumerate(top_chunks):
            context_lines.append(f"【自定义商品库/话术匹配段 {idx+1}】 (匹配度 {sim:.2f}):\n{text}")
        return "\n\n".join(context_lines)
    except Exception as e:
        logger.error(f"Failed to calculate custom RAG context: {e}")
        return ""

